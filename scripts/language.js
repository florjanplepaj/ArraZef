// Function to update the language based on the selected language
function updateLanguage(language) {
  const langData = translations[language];

  if (!langData) return;

  const bodyText = document.querySelector(".js-text-body-info");
  if (bodyText) bodyText.innerText = langData.bodyText;

  const textTitle = document.querySelector(".js-text-title");
  if (textTitle) textTitle.innerText = langData.textTitle;

  const statsElements = document.querySelectorAll(".js-statistics-text-text");
  if (statsElements.length > 0) {
    statsElements.forEach((el, index) => {
      el.innerText = langData.statistics[index];
    });
  }

  const servicesElements = document.querySelectorAll(
    ".js-services-kind-up-text-text"
  );
  if (servicesElements.length > 0) {
    servicesElements.forEach((el, index) => {
      el.innerText = langData.services[index];
    });
  }

  const serviceTitlesElements = document.querySelectorAll(
    ".js-services-kind-up-text"
  );
  if (serviceTitlesElements.length > 0) {
    serviceTitlesElements.forEach((el, index) => {
      el.innerText = langData.serviceTitles[index];
    });
  }

  const contactUSTitle = document.querySelector(".footer-center-titile");
  if (contactUSTitle) contactUSTitle.innerHTML = langData.contactUSTitle;

  const addressTitle = document.querySelector(".footer-right-titile");
  if (addressTitle) addressTitle.innerHTML = langData.addressTitle;

  const companyTitle = document.querySelector(".footer-right-menu-title");
  if (companyTitle) companyTitle.innerHTML = langData.companyTitle;

  const menuButtonsElement = document.querySelectorAll(
    ".footer-right-menu-body-text-text"
  );
  if (menuButtonsElement.length > 0) {
    menuButtonsElement.forEach((el, index) => {
      el.innerText = langData.menuButtons[index];
    });
  }

  const menuButtonsElementTop = document.querySelectorAll(".js-about-text");
  if (menuButtonsElementTop.length > 0) {
    menuButtonsElementTop.forEach((el, index) => {
      el.innerText = langData.menuButtons[index];
    });
  }

  const titleCoverText = document.querySelector(".js-title-cover-text");
  if (titleCoverText) titleCoverText.innerText = langData.recentText[0];

  const buttonViewAllText = document.querySelector(".js-button-view-all-text");
  if (buttonViewAllText) buttonViewAllText.innerText = langData.recentText[1];

  const recentProjectViewProjectButtons = document.querySelectorAll(
    ".js-recent-project-view-project-button"
  );
  if (recentProjectViewProjectButtons.length > 0) {
    recentProjectViewProjectButtons.forEach((button) => {
      button.innerText = langData.recentText[2];
    });
  }
  const servicesUperTitle = document.querySelector(".js-services-uper-title");
  if (servicesUperTitle) servicesUperTitle.innerText = langData.recentText[3];

  const aboutUsTitleTop = document.querySelector(".about-us-top-title");
  if (aboutUsTitleTop) aboutUsTitleTop.innerText = langData.aboutUsTop[0];
  const aboutUsBodyTop = document.querySelector(".about-us-top-sub-title");
  if (aboutUsTitleTop) aboutUsBodyTop.innerHTML = langData.aboutUsTop[1];

  const projectTitleTop = document.querySelector(".projects-pro-top-title");
  if (projectTitleTop) projectTitleTop.innerText = langData.projectTop[0];
  const projectBodyTop = document.querySelector(".projects-pro-top-sub-title");
  if (projectBodyTop) projectBodyTop.innerHTML = langData.projectTop[1];
  const projectViewButtons = document.querySelectorAll(
    ".js-projects-page-view-project-button"
  );

  if (projectViewButtons.length > 0) {
    projectViewButtons.forEach((button) => {
      button.innerHTML = langData.projectTop[2];
    });
  }

  const servicesBodyTop = document.querySelector(".service-top-title");
  if (servicesBodyTop) servicesBodyTop.innerHTML = langData.serviceTop[0];

  const servicesTitleTop = document.querySelector(".service-top-sub-title");
  if (servicesTitleTop) servicesTitleTop.innerText = langData.serviceTop[1];
  const servicesBodyTopTitle = document.querySelector(".service-top-subtitle");
  if (servicesBodyTopTitle)
    servicesBodyTopTitle.innerHTML = langData.serviceTop[2];

  const contactTitleTop = document.querySelector(".contact-pro-top-title");
  if (contactTitleTop) contactTitleTop.innerText = langData.contactTop[0];
  const contactsBodyTopTitle = document.querySelector(
    ".contact-pro-top-sub-title"
  );
  if (contactsBodyTopTitle)
    contactsBodyTopTitle.innerText = langData.contactTop[1];

  const contactsBody = document.querySelector(".js-form-right-div");
  if (contactsBody) contactsBody.innerHTML = langData.contactForm;

  const servicesTitleTopElement = document.querySelectorAll(
    ".js-service-body-title"
  );
  if (servicesTitleTopElement.length > 0) {
    servicesTitleTopElement.forEach((el, index) => {
      el.innerText = langData.serviceTextTitle[index];
    });
  }

  const contactTrioElement = document.querySelectorAll(".js-constact-title");
  if (contactTrioElement.length > 0) {
    contactTrioElement.forEach((el, index) => {
      el.innerText = langData.contactMeth[index];
    });
  }

  const projectTuttiEle = document.querySelector(
    ".js-selected-project-left-all-projects"
  );
  if (projectTuttiEle) projectTuttiEle.innerText = langData.projectLoc;
}

// Function to handle Italian language selection
function languageIt() {
  document.querySelector(".js-flag-2").addEventListener("click", () => {
    updateLanguage("it");
  });
}

// Function to handle English language selection (if needed)
function languageEn() {
  document.querySelector(".js-flag-1").addEventListener("click", () => {
    updateLanguage("en");
  });
}

languageEn();
languageIt();

// Store language preference in localStorage
function setLanguage(language) {
  localStorage.setItem("selectedLanguage", language);
  updateLanguage(language);
}

// Load language from localStorage when the page loads
function loadLanguagePreference() {
  const savedLanguage = localStorage.getItem("selectedLanguage") || "en";
  updateLanguage(savedLanguage);
}

// Call loadLanguagePreference on page load
window.addEventListener("load", loadLanguagePreference);

// Event listeners for language buttons
document
  .querySelector(".js-flag-2")
  .addEventListener("click", () => setLanguage("it"));
document
  .querySelector(".js-flag-1")
  .addEventListener("click", () => setLanguage("en"));
